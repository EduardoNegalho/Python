'''
Em programação os tipos de dados são importantes, 
e em python temos diversos tipos.
'''

# tipo de texto
text = 'Hello World'
print(type(text))

# tipo número inteiro 
number = 20
print(type(number))

# tipo número decimal
decimal = 0.99
print(type(decimal))

# tipo verdadeiro ou falso
boolean_true = True
boolean_false = False
print(type(boolean_true))
print(type(boolean_false))