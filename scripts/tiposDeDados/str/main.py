var = 'Hello World!'

# fatiamento de str
# [inicio : final : passo]

# pega do indice até o final
print(var[6:])

# pega do indice inicial até o indice final exclusivo
print(var[0:5])

# pega do inicio até o final pulando de 2 em 2
print(var[0:: 2])

# pega do final até o inicio
nome = 'eduardo'
print(nome[::-1])

# tamanho
print(len(var))