# Tipagem = Dinâmica / Forte
# O python já detecta o tipo de dado que foi passado
# Tudo que esta dentro de aspas é um str(string)

# Aspas simples
print('Eduardo Marques')
print('Eduardo "Marques"')

# Aspas duplas
print("Eduardo Marques")
print("'Eduardo' Marques")

# Escape
print("Eduardo \"Marques\"")

# r
print(r"Eduardo \"Marques\"")
